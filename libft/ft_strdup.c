#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str1;
	char	*str2;
	int		size;
	int		i;

	str1 = (char *)s1;
	size = ft_strlen(str1) + 1;
	str2 = (char *)malloc(size);
	i = 0;
	if (str2 == NULL)
		return (NULL);
	while (str1[i] != '\0')
	{
		str2[i] = str1[i];
		i++;
	}
	str2[i] = '\0';
	return (str2);
}
